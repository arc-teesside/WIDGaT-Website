<h2>Persona</h2>
 This is for an academic tutor new to online learning and using the VLE, who needs help in planning their module and requires guidance on implementing recommendations.
<h2>Scenario</h2>
 The widget would be developed by a learning technologist and sent to an academic tutor before the module was due to be delivered. The tutor can then use the widget to guide themselves through some basic VLE tasks. Current guidance materials are not ordered and require the tutor to search for the appropriate guide or help video.  The widget will allow for guidance to be easily accessed and framed within a structured process of a &#8216;checklist&#8217;.
