</div>
<ol>
	<li><b>Component list</b><br/>Drag and Drop a component in the widget editor window (2) to add it. Components can only be dropped into a placeholder.</li>
	<li><b>Widget editor window</b><br/>Select components here to edit it's properties and style. The frame include two button: 'Preview' and 'Refresh'</li>
	<li><b>Style customisation</b><br/>You can customise your widget and component's style here</li>
	<li><b>Component's properties</b><br/>When a component is selected in the widget editor window (2) it's properties will appear here (see '<a href="index.php?p=guides/howto">Using WIDGaT</a>' guide for more information)</li>
	<li><b>Guidance list</b><br/>This panel will let you know if you need to do something in order to complete your widget</li>
</ol>
